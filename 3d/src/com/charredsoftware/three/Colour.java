package com.charredsoftware.three;

public class Colour {

	public float red, green, blue, alpha;
	
	public Colour(float red, float green, float blue){
		this.red = red;
		this.blue = blue;
		this.green = green;
	}
	
	public Colour(float red, float green, float blue, float alpha){
		this.red = red;
		this.blue = blue;
		this.green = green;
		this.alpha = alpha;
	}
	
}
